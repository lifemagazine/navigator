package com.lifemagazine.navigator.rest;

public class Cost {

	private double air;
	private double sea;
	private double road;
	private double rail;
	
	public double getAir() {
		return air;
	}
	public void setAir(double air) {
		this.air = air;
	}
	public double getSea() {
		return sea;
	}
	public void setSea(double sea) {
		this.sea = sea;
	}
	public double getRoad() {
		return road;
	}
	public void setRoad(double road) {
		this.road = road;
	}
	public double getRail() {
		return rail;
	}
	public void setRail(double rail) {
		this.rail = rail;
	}
	
}
